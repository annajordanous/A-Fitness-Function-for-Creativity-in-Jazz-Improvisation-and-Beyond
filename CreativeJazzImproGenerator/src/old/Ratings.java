package old;

public class Ratings {
	
	double[] value;
	double[] typicality;
	
	public Ratings(int size)  {
		value = new double[size];
		typicality = new double[size];
	}
	
	void setValueRatingAt(double valueRating, int index)  {
		value[index] = valueRating;
	}
	
	void setTypicalityRatingAt(double typicalityRating, int index)  {
		typicality[index] = typicalityRating;
	}
	
	double getValueRatingAt(int index)  {
		return value[index];
	}
	
	double getTypicalityRatingAt(int index)  {
		return typicality[index];
	}
}
